#每月存1000元,连续存十年,第一个1000元存了120个月,第二个1000元存了119个月
#以此类推,等价于1000元存了7260个月,月化利率为lilv,十年后本息之和为profit
#若十年后能取出共二十万元,运行结果显示月化利率为1.102%,则年化利率为13.224%
#完全高出预期,甚至高出宣传的10%,且此算法还不包括保单自身的价值
def Profit(lilv):
    profit=1000*7260*lilv+120000
    return profit
lilv=0.00000001
while Profit(lilv)<200000:
    lilv+=0.00000001
print('月化利率为{:.5f}'.format(lilv))
    
    
